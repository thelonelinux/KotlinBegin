package Package

fun main(){
    //Variable in kotlin
    //var keyword for mutable keywords
    //val keyword for immutable keywords
    var temp:Int=10
    println(temp)
    temp=30
    println(temp)

    val name:String="Dhone"
    println(name)

    var i:Int
    i=22
    println(i)


    //Kotlin can also self identify the data type and store the value
    var num=50;
    println(num)
}